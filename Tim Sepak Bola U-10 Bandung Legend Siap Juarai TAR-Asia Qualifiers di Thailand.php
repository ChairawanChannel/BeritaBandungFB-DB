        <div class="detailcontent text-black">
            <p>Kapten Bandung Legend, Adrian Alninemar Airlangga atau yang kerap disapa Nemar mengatakan, ia dan timnya telah mempersiapkan diri untuk bertanding melawan tim lainnya di kancah internasional.</p>

            <p>&quot;Sudah latihan selama sebulan ini. Sudah persiapan baju, sepatu, peralatan lainnya, dan uang,&quot; ujar Nemar saat ditemui pada acara pelepasan tim di Pendopo Kota Bandung, Selasa 12 Desember 2023.</p>

            <p>Agar semakin solid, Nemar mengatakan, timnya perlu bermain sportif dan tetap tenang.</p>

            <p>&quot;Buat bikin solid tim itu kita main sportif saja, jangan terbawa emosi,&quot; ucapnya</p>

            <p>Ia berharap, bersama tim Bandung Legend bisa membawa Indonesia menjadi juara dan mampu membanggakan orang tua.</p>

            <p>Serupa dengan Nemar, salah satu best player U-10 Kota Bandung, Juan Felyx Adam, menyebutkan ia dan timnya akan berangkat pada Kamis 14 Desember 2023.&nbsp;</p>

            <p>&quot;Persiapannya dari perlengkapan untuk pergi, makanan, dan uang sudah ditanggung pihak sekolah sepak bola (SSB). Kami juga sudah latihan dari Senin-Sabtu, mulai pukul 16.00-18.00 WIB. Hari Minggu libur,&quot; ungkap Juan.</p>

            <p>Ia mengaku senang bisa mewakili Indonesia ke Thailand. Sebab ini pertama kalinya ia bertanding di luar negeri.&nbsp;</p>

            <p>&quot;Saya jadi bek di tim sepak bola. Cita-citanya jadi pemain bola profesional. Harapannya, bisa jadi juara di pertandingan nanti dan membanggakan orang tua,&quot; ucapnya.</p>

            <p>Sementara itu, Pj Wali Kota Bandung, Bambang Tirtoyuliono menyampaikan rasa bangganya terhadap generasi muda yang siap mengharumkan nama baik Kota Bandung.</p>

            <p>Ia berjanji akan memaksimalkan fasilitas bagi para atlet dan membantu agar pendidikan mereka tetap bisa berjalan lancar meski disibukkan dengan latihan sepak bola.</p>

            <p>&quot;Saya atas nama Pemkot Bandung mendoakan dan meminta maaf jika fasilitas saat ini dirasa belum maksimal. Insyaallah ke depannya akan lebih maksimal terutama atlet usia dini,&quot; tutur Bambang.</p>

            <p>&quot;Saya akan komunikasikan dengan Kadisdik, selama bertanding anak-anak kita tidak terganggu pendidikan formalnya. Akan kita cari jalan keluarnya,&quot; imbuhnya.</p>

            <p>Ia juga berpesan agar para pemain tak perlu terlalu merasa terbebani dengan juara. Namun, bertanding dengan jiwa sportif adalah kunci yang penting.</p>

            <p>&quot;Jangan pernah berpikir untuk menang. Tapi bertandinglah dengan maksimal. Jiwa sportif itu yang menjadi penting,&quot; pintanya.</p>

            <p>Sedangkan Kepala Dinas Pemuda dan Olahraga (Dispora) Kota Bandung, Eddy Marwoto memaparkan, tim U-10 ini telah menjuarai level regional Jawa Barat saat bertanding di Bogor. Tak hanya itu, tim Bandung Legend U-10 juara tingkat nasional di Bali.</p>

            <p>&quot;Para juara inilah yang akhirnya dikirimkan untuk bertanding ke Bangkok, Thailand tanggal 16-17 Desember 2023. Tak ada hentinya PSSI menyelenggarakan kompetisi untuk memunculkan bibit-bibit baru atlet sepak bola di Kota Bandung,&quot; ungkap Eddy.</p>

            <p>Negara yang akan bertanding di liga TAR-Asia Qualifiers mendatang di antaranya Thailand, China, Jepang, Malaysia, Singapura, Vietnam, Kamboja, Indonesia, dan Laos. Tim Bandung Legend masuk dalam grup F bersama club dari Vietnam dan beberapa tim Thailand.</p>

            <p>&quot;Mudah-mudahan anak-anak ini juara. Nanti jika menang, akan dikirimkan ke pertandingan di Barcelona yang dilaksanakan pada 23-29 April 2024,&quot; jelasnya.</p>

            <p>Ia menambahkan, pertandingan tersebut merupakan peluang dan inovasi bagi Kota Bandung untuk rutin membuat kegiatan pertandingan yang berjenjang dan berkesinambungan pada usia dini. (din)**</p>

