        <div class="detailcontent text-black">
            <p>Pemerintah Kota (Pemkot) Bandung resmi memperkenalkan wajah baru penataan PKL Basemen Alun-alun, Kamis (21/12/2023). Penataan tersebut diresmikan langsung Pj Wali Kota Bandung, Bambang Tirtoyuliono.</p>
            <p>Sebanyak 140 PKL dari kawasan Dalem Kaum dan Alun-alun telah menempati tempat baru.</p>
            <p>Bambang mengatakan, penataan ini untuk mendukung keindahan, ketertiban dan kenyamanan lingkungan di sekitar Jalan Dalem Kaum dan Alun-alun.</p>
            <p>Kawasan Dalem Kaum dan Alun-alun merupakan zona merah PKL menurut Peraturan Daerah Nomor 4 tahun 2011 tentang penataan dan pembinaan PKL.</p>
            <p>"Kita ingin menghadirkan kota Bandung sebagai kota yang indah. Para pelaku usaha informal harus taat asas karena Kota Bandung punya Perda. Ada satu di antaranya kita relokasi," kata Bambang.</p>
            <p>"Sehingga kenyamanan keindahan bisnis informal pun bisa jalan, salah satu spotnya di Alun-alun. Ini adalah solusi yang terbaik," imbuhnya.</p>
            <p>Ia menyebut penataan PKL Basemen Alun-alun dapat menjadi salah satu destinasi wisata kuliner baru di Kota Bandung. Penataan dapat meningkatkan omzet UMKM dan mengundang lebih banyak wisatawan.</p>
            <p>Bambang pun mengapresiasi komitmen para PKL yang mau untuk ditata ke Basemen Alun-alun dan tidak akan kembali lagi berjualan di kawasan zona merah.</p>
            <p>"Tadi sudah ada komitmen dari PKL yang direlokasi tidak akan kembali lagi bahkan akan mengajak PKL yang lain kooperatif dengan kita untuk menghadirkan kota Bandung yang indah," ujarnya.</p>
            <p>Sekretaris Daerah Kota Bandung selaku Ketua Satgasus PKL, Ema Sumarna mengatakan penataan Kawasan Alun-alun adalah perjalanan panjang sejak 10-12 tahun yang lalu.</p>
            <p>"Alhamdulillah ada 140 pelaku usaha informal yang sudah kita verifikasi dan validasi," ujarnya.</p>
            <p>Ema menyebut penataan PKL Basement Alun-alun bekerjasama dengan PT. Mayora yang telah memberikan daya dukung sarana berjualan sebanyak 140 kios, kursi dan meja, exhouse, kipas, apar, lampu 140 titik, wastafel, neon boks, pelaburan dinding, serta daya dukung instalasi air dan listrik.&nbsp;</p>
            <p>"Walaupun masih ada beberapa titik yang kurang, nanti kita perbaikan. Kita doakan sektor usaha informal bisa naik kelas ke sektor formal," katanya.</p>
            <p>Pada kesempatan tersebut, PKL Basemen Alun-alun berkomitmen untuk menjaga ketertiban dan kebersihan; menjaga sarana dan prasarana yang telah difasilitasi; tidak akan memperdagangkan barang ilegal; tidak akan merombak menambahkan mengubah fungsi fasilitas yang ada; tidak akan meminjamkan menyewakan memindahkan tempat berjualan kepada pihak lain; dan perlengkapan dagang mudah dipindahkan.&nbsp;</p>
            <p>Komitmen tersebut dibacakan oleh Koordinator PKL Basemen Alun-alun, Ishak. (rdp*)</p>

