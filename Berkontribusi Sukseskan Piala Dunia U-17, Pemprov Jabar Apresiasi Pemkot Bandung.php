    <div class="detailcontent text-black">
      <p>Apresiasi itu diberikan oleh Penjabat Gubernur Jawa Barat, Bey Triadi Machmudin kepada Penjabat Wali Kota Bandung, Bambang Tirtoyuliono, pada acara Malam Apresiasi Piala Dunia U-17 Indonesia 2023 di Wilayah Jawa Barat, di Sport Jabar Arcamanik, Selasa 19 Desember 2023 malam.&nbsp;</p>

      <p>&quot;Alhamdulillah kita (Pemkot Bandung) mendapatkan apresiasi dari Pemprov Jabar. Ini sebagai bentuk dukungan kita terhadap dunia olahraga,&quot; kata Penjabat Wali Kota Bandung, Bambang Tirtoyuliono.&nbsp;</p>

      <p>Ia berharap apresiasi ini menjadi motivasi untuk terus mendukung dunia olahraga.&nbsp;</p>

      <p>&quot;Mudah-mudahan ini menjadi dorongan bagi kita untuk terus mendukung memberikan ruang yang layak bagi dunia olahraga,&quot; ujarnya.&nbsp;</p>

      <p>Di tempat yang sama, Kepala Dinas Pemuda dan Olahraga Kota Bandung, Eddy Marwoto mengucapkan syukur atas apresiasi dari Pemprov Jabar.</p>

      <p>&quot;Kita mendapatkan apresiasi, selama ini terus mendukung Piala Dunia U-17. Kita punya aset seperti Stadion Sidolig, itu dikategorikan sudah menjadi standar internasional. Ini motivasi kita untuk pengembangan aset lainnya,&quot; beber Eddy.&nbsp;</p>

      <p>Menurutnya, Pemkot Bandung berencana soal pemanfaatan aset olahraga agar lebih optimal. Baik untuk masyarakat maupun digunakan untuk kegiatan olahraga lainnya.&nbsp;</p>

      <p>&quot;Kita berbicara dengan Pemprov Jabar, meminta saluran bantuan lain dari provinsi atau Kementerian PUPR,&quot; ujarnya.&nbsp;</p>

      <p>Tahun depan, lanjut Eddy, Pemkot Bandung tetap fokus meningkatkan pengembangan aset infrastruktur sarana olahraga yang ada.&nbsp;</p>

      <p>&quot;Tahun 2024 kita tetap fokus meningkatkan pengembangan infrastruktur aset sarana olahraga yang ada. Ini motivasi kita untuk standar yang sama seperti Sidolig,&quot; tegasnya.&nbsp;</p>

      <p>Sementara itu, Penjabat Gubernur Jawa Barat, Bey Triadi Machmudin mengaku, selama pelaksaan turnamen sepak bola itu, turut memantau dan memastikan kelayakan sarana olahraga di Jabar.</p>

      <p>&quot;Para pelatih hadir itu menguji, hasilnya mereka puas karena suasana nyaman dan banyak lapangannya. Ini berkat dukungan semua pihak &quot; ungkapnya.&nbsp;</p>

      <p>Ia menegaskan, merawat sarana olahraga itu sudah selayaknya dilaksanakan dengan rutin sehingga terpelihara dengan baik.&nbsp;</p>

      <p>&quot;Berhasil melaksanakan dengan baik, tempat tanding dan latihan. Tapi kita jangan lupa merawatnya. Saya minta Kepala Dinas Pemuda dan Olahraga untuk mengelola stadion agar produktif dan terpelihara selamanya,&quot; bebernya.&nbsp;</p>

      <p>Berikut apresasi yang diberikan oleh Pemprov Jabar kepada para pengelola lapangan, Pemerintah Daerah, unsur institusi pendidikan hingga Pemerintah Pusat:</p>

      <p>1. Pengelola Stadion Sepak Bola Si Jalak Harupat&nbsp;<br />
        2. Pengelola Stadion Sepak Bola Sidolig&nbsp;<br />
        3. Pengelola Stadion Sepak Bola Sport Jabar Arcamanik&nbsp;<br />
        4. Pengelola Stadion Sepak Bola IPDN&nbsp;<br />
        5. Pengelola Stadion Sepak Bola ITB&nbsp;<br />
        6. Pengelola Stadion Sepak Bola Unpad&nbsp;<br />
        7. Pemerintah Daerah Kabupaten Bandung&nbsp;<br />
        8. Pemerintah Kota Bandung&nbsp;<br />
        9. Pemerintah Daerah Kabupaten Sumedang&nbsp;<br />
        10. Kementerian Pekerjaan Umum dan Perumahan Rakyat. (yan)**</p>

