    <div class="detailcontent text-black">
      <p>&quot;Alhamdulillah, masa uji coba ini sudah berjalan, walaupun belum ideal. Kami baru menggunakan 9 biofon untuk maggotisasi. Untuk sampah anorganik, 4 dari 6 konveyor sudah berjalan. Untuk anorganik yang belum sampai ke Lawe akan diangkut ke Cukang Holis,&quot; ujar Sekretaris Daerah Kab. Bandung, Ema Sumarna saat melakukan pemantauan ke TPST Gedebage, Rabu 6 Desember 2023.</p>

      <p>&quot;Idealnya kita punya mesin Gibrik untuk pencacahan dan pengolahan menjadi RDF. Sampah yang memiliki nilai ekonomis dipilah oleh petugas ini untuk memberikan penghasilan tambahan bagi mereka,&quot; tambahnya.</p>

      <p>Ema mengatakan saat ini masih ada beberapa catatan, terutama terkait hanggar belatung. Ia mengatakan belatung harus dipelihara dalam kondisi yang tidak panas dan tanpa air.</p>

      <p>Untuk itu, saat ini sedang dipasang paranet agar hanggar terlindung dari sinar matahari langsung.</p>

      <p>&quot;Setelah kami lihat di lapangan dan masukan dari pegiat belatung, kami harus menggunakan penutup dinding yang bisa menyerap air, bukan terpal, karena belatung katanya tidak bisa makan dalam kondisi panas,&quot; kata Ema.</p>

      <p>&quot;Atapnya masih belum sempurna, kami harus menggunakan paranet. Nah pemasangan paranet ini harus selesai satu sampai dua minggu lagi, karena belatung tidak bisa makan dalam kondisi panas,&quot; lanjutnya.</p>

      <p>Selain itu, untuk menghindari hama seperti tikus dan burung, akan dibangun benteng dan dipasang paving block.</p>

      <p>&quot;Kalau ada tikus yang masuk, itu belatung. Makanya kita akan tembok ulang di pinggir-pinggirnya. Nanti kita juga akan pasang paving blok agar nyaman. Jangan sampai ada air yang bocor, belatung tidak nyaman dengan panas dan air,&quot; katanya.</p>

      <p>Ema mengatakan bahwa belatung efektif untuk menguraikan sampah makanan. Untuk itu, pihaknya akan mendorong agar sampah makanan kafe dan restoran diolah di TPST Gedebage.</p>

      <p>&quot;Kami ingin semua sampah organik restoran di Kota Bandung dibawa ke sini (TPST Gedebag). Nanti bisa dicampur dengan sampah pasar dan sampah restoran. Karena sampah makanan kesukaan belatung adalah makanan manusia. Satu kotak biofon idealnya mencapai 1 kuintal, semakin banyak makanan, semakin banyak belatungnya.&quot; &quot; katanya.</p>

      <p>Secara keseluruhan, selama uji coba, sampah yang bisa diolah mencapai 10 ton.</p>

      <p>Ema mengatakan, Pemerintah Kota Bandung telah menerapkan berbagai skema untuk bisa mengolah sampah. Mulai dari hanggar belatung di 151 kelurahan, Kang Empos untuk 20 persen keluarga di setiap kelurahan dan skema lainnya.</p>

      <p>&quot;Dalam sehari kami bisa mengolah 20 ton sampah organik sisa makanan untuk memenuhi kebutuhan sampah organik sisa makanan. Kita juga punya hanggar di 151 kecamatan yang bisa mengolah 151 ton sehingga total kita bisa mengolah 171 ton sampah organik jika semuanya efektif,&quot; katanya.</p>

      <p>&quot;Jadi kalau kita ingin mengurangi 550 ton sampah, 40 persennya bisa diolah dengan maggot ini. Belum lagi Kang Empos ini berjalan, kalau sudah jadi di Bandung akan ada 20 persen keluarga di Kota Bandung, saya rasa ini logis. Walaupun ini baru berjalan 10 persen,&quot; tambahnya.</p>

      <p>Ema juga menegaskan bahwa kehadiran TPST Gedebage tidak akan menimbulkan pencemaran lingkungan di daerah tersebut.</p>

      <p>&quot;Dan ini juga tidak mencemari lingkungan, dengan teknologi yang digunakan tidak akan menjadi pencemaran lingkungan. Karena sudah dibicarakan terkait hal itu,&quot; katanya.</p>

      <p>Saat ini, Pemkot masih memiliki kuota 13.000 rit untuk TPA Sarimukti atau sekitar 65.000 ton. Melihat perkembangan yang ada, Ema optimis sebelum akhir tahun TPST Gedebage akan beroperasi maksimal dan status Darurat Sampah akan segera berakhir.</p>

      <p>&quot;Alternatifnya banyak, kalau kita terjebak pada satu alternatif saja akan menyulitkan kita. (rob)**</p>

      <p>&nbsp;</p>

