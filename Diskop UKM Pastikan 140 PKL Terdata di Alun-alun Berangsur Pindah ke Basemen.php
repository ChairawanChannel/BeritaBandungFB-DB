    <div class="detailcontent text-black">
      <p>Di sisi lain, Pemkot Bandung juga telah menyiapkan solusi yakni penataan dan relokasi PKL ke Basemen Alun-alun. Total 140 PKL telah terdata dan berangsur dipindahkan ke basemen yang juga beberapa hari lalu telah diresmikan Pemkot Bandung.</p>

      <p>&quot;PKL yang terdata untuk dipindahkan sebanyak 140. Dari 140 PKL tersebut, masih ada sekitar 52 PKL yang belum pindah. Lalu dari 52 PKL yang belum pindah tadi, informasi terakhir yang kami terima, sekitar 20 PKL sudah berangsur turun ke basemen. Sehingga ada sekitar 32 PKL lagi yang masih dalam proses berpindah ke area basemen,&quot; ujar Atet.</p>

      <p>Atet juga menyebut, area PKL di Basemen Alun-alun diperuntukkan bagi PKL terdata yang menjual kuliner dan non kuliner. Termasuk PKL yang berjualan di kawasan Jalan Dalem Kaum yang telah direlokasi.</p>

      <p>Terkait penolakan dari sejumlah PKL untuk direlokasi, Atet meminta semua pihak menyadari dan menaati regulasi yang telah ditetapkan.</p>

      <p>&quot;Kawasan Alun-alun itu sudah ditetapkan sebagai zona merah PKL. Kami siapkan tempat untuk relokasinya, lalu sudah ada PKL terdata sebanyak 140. Kami juga perlu mencari tahu apakah penolakan itu datangnya dari PKL yang sudah terdata atau bukan. Karena PKL yang sudah terdata, itu sudah berangsur pindah ke basemen,&quot; terangnya.</p>

      <p>Atet juga meminta para pedagang yang telah berangsur turun ke area basemen agar tidak terprovokasi. Ia memastikan, fasilitas dan sarana yang telah disiapkan di Basemen Alun-alun akan bermanfaat positif, baik bagi para pedagang maupun pembelinya.</p>

      <p>&quot;Bagi pedagang yang sudah masuk pendataan, sudah turun ke bawah, diharapkan tidak terprovokasi. Coba dilihat dulu fasilitas yang sudah kami siapkan. Kami pastikan kenyamanan pedagang dan pembelinya terjaga,&quot; katanya.</p>

      <p>Selanjutnya, Diskop UKM akan berkoordinasi dengan Satpol PP terkait pendataan PKL untuk merinci ulang PKL terdata. Baik itu PKL yang sudah mulai berjualan, maupun PKL yang masih berproses untuk pindah.</p>

      <p>&quot;Kami perlu berkoordinasi kembali dengan Satpol PP. Kami sudah menghitung sebagian besar pedagang sudah turun dan berpindah lapak jualan ke basemen,&quot; tutur Atet. (ray)**</p>

