    <div class="detailcontent text-black">
      <p>Hal itu disampaikan Kepala Dinas Kebudayaan dan Pariwisata (Disbudpar) Kota Bandung, Arif Syaifudin di Kantor DKPP Kota Bandung, Kamis 21 Desember 2023.</p>

      <p>&quot;Area cagar budaya di kantor DKPP ini aman. Tidak akan terpengaruh proyek strategis nasional. Hanya saja ini ada jalan masuk (pintu masuk di sebelah pos pengamanan) yang akan kita geser. Nanti akan dibangun ulang jalan masuk baru di sekitarnya, kurang lebih 12 meter bergeser untuk manuver kendaraan keluar masuk,&quot; jelasnya.</p>

      <p>Ia juga memastikan, fungsi bangunan cagar budaya tidak akan terpengaruh apapun dan bisa digunakan.</p>

      <p>&quot;Tidak terpengaruh. Intinya bangunan cagar budaya di sini aman,&quot; terangnya.</p>

      <p>Terpisah, Kepala DKPP Kota Bandung Gin Gin Ginanjar menyebut, Tim Analis Cagar Budaya (TACB) Kota Bandung sudah mengeluarkan rekomendasi agar bangunan cagar budaya di Kantor DKPP Kota Bandung tidak boleh dibongkar.</p>

      <p>Di sisi lain, ia menjelaskan eksisting pintu masuk yang ada saat ini digunakan untuk manuver kendaraan untuk pemotongan hewan.</p>

      <p>Meski begitu, solusi yang didapat antara lain dengan membangun pintu masuk baru untuk manuver kendaraan keluar dan masuk. Sedangkan pintu masuk yang sudah ada dibiarkan namun dipagar permanen, karena merupakan bagian yang tidak terpisahkan dari bangunan cagar budaya.</p>

      <p>&quot;Tentu dampaknya ada (dari konteks ruang). Namun secara teknis sudah dibicarakan sehingga selain tidak mengganggu aspek cagar budayanya, juga tidak mengganggu juga kinerja kita,&quot; terang Gin Gin.</p>

      <p>Sebagai informasi, fly over dan JPO Ciroyom ditargetkan selesai pada Desember 2023. Sehingga pada awal 2024, masyarakat Kota Bandung dapat merasakan manfaatnya.</p>

      <p>Kehadiran fly over dan JPO ini bisa memudahkan operasional feeder Kereta Cepat Jakarta Bandung (KCJB). (ray)**</p>

      <p><br />
        Kepala Diskominfo Kota Bandung</p>

