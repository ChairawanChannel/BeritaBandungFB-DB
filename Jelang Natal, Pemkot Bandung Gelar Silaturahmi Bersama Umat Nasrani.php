        <div class="detailcontent text-black">
            <p>&quot;Ini sebuah role model, saya yakin Kota Bandung akan dijadikan contoh kota besar lainnya di nasional,&quot; ujar Bambang Tirtoyuliono pada acara Silaturahmi bersama Umat Nasrani Menjelang Hari Raya Natal di Pendopo Kota Bandung, Rabu 20 Desember 2023 malam.</p>

            <p>Sebagai penguatan, Bambang mengungkapkan, di Kota Bandung terdapat Kampung Toleransi. Setiap kawasan memiliki perbedaan agama dan sebagainya. Namun mampu menjalankan kehidupan dengan rukun serta saling menghargai.&nbsp;</p>

            <p>&quot;Kita punya lima kampung toleransi, ini salah satu bentuk manifestasi. Kerukunan antar umat beragama ini sampai dengan level wilayah,&quot; katanya.</p>

            <p>Perlu diketahui, Kampung Toleransi di Kota Bandung di antaranya, Kampung Toleransi Gang Luna, Kecamatan Bojongloa Kaler. Kampung Toleransi Paledang, Kecamatan Lengkong. Kampung Toleransi Dian Permai, Kecamatan Babakan Ciparay. Kampung Toleransi Balonggede, Kecamatan Regol. Kampung Toleransi Kebon Jeruk, Kecamatan Andir.&nbsp;</p>

            <p>&quot;Bahwa pluralisme ini sebuah kekuatan mampu menjadikan sebagai pemersatu, salah satunya kerukunan umat agama,&quot; tuturnya.&nbsp;</p>

            <p>Sementara itu, Mejelis Agama Katolik Kota Bandung, Romo Agustinus Sugiharto mengucapkan terima kasih kepada Pemkot Bandung yang telah memberikan ruang untuk silaturahmi menjelang hari kedamaian itu.&nbsp;</p>

            <p>&quot;Tema natal tahun ini Kemuliaan Bagi Allah dan Damai Sejahtera di Bumi. Mudah - mudahan hari natal tahun ini memberikan kedamaian dan kebahagiaan kepada umatnya,&quot; ungkap Agustinus.</p>

            <p>Ia mengungkapkan, jumlah jemaah Katolik di Kota Bandung mencapai 56 ribu. Atas hal itu, jemaahnya mendukung berbagai program Pemkot Bandung.&nbsp;</p>

            <p>&quot;Kami mendukung berbagai program yang telah dirancang oleh Pemkot Bandung. Kami sama - sama hendak mewujudkan Bandung yang aman, nyaman, kondusif dan rukun aman serta toleran,&quot; ungkapnya.&nbsp;</p>

            <p>Di tempat yang sama, Majelis Agama Kristen Kota Bandung, Pendeta Yosafat Simatupang mengungkapkan, bulan Desember merupakan bulan kebahagiaan bagi umat Kristiani.&nbsp;</p>

            <p>&quot;Perayaan natal bukan hanya merayakan suka cita saja, tapi pengorbanan, ada perjuangan,&quot; ungkapnya.&nbsp;</p>

            <p>Ia pun mengapresiasi kinerja Pemkot Bandung yang selama ini meraih prestasi.&nbsp;</p>

            <p>&quot;Saya follow instagram Humas Bandung, di situ ada informasi terkait UHC meningkat, apresiasi kepada pemuda, bahkan saya suka Bandung ini Best Street Foods dan Best Snacks. Saya rasa pencapaian oleh pemerintah ini banyak lagi,&quot; tuturnya.&nbsp;</p>

            <p>Soal sampah, jemaaahnya pun ikut andil dalam penanganan sampah. Ia akui saat ini tengah gencar dalam pengelolaan sampah dengan bank sampah.&nbsp;</p>

            <p>&quot;Kami gencar dengan bank sampah. Gereja hadir bukan hanya ibadah, tapi saat ini memilah sampah. Meskipum demikian apa yang kita lakukan menjadi berkat untuk banyak orang,&quot; ungkapnya.&nbsp;</p>

            <p>Sedangkan Ketua Forum Kerukunan Umat Beragama Kota Bandung, Ahmad Suherman menegaskan agar tempat ibadah jangan digunakan sebagai sarana untuk kampanye.&nbsp;</p>

            <p>&quot;Saya sampaikan kembali kepada para tokoh agama, mohon jangan sekali - kali rumah ibadah dijadikan arena kampanye. Kita harapkan rumah ibadah membawa suasana yang dingin sejuk dan bisa merangkul kita semuanya,&quot; tegas Suherman.</p>

            <p>Menurutnya, bagaimanapun juga Kota Bandung masyarakatnya sudah rukun dan damai. Hal ini, lanjut Suherman perlu dirawat oleh masyarakat hingga unsur yang terkait semua.&nbsp;</p>

            <p>&quot;Tidak hanya pemerintah saja, masyarakat dimohon untuk berperan serta soal kerukunan di Kota Bandung,&quot; tuturnya. (yan)**</p>

            <p><br />
                Kepala Diskominfo Kota Bandung</p>

