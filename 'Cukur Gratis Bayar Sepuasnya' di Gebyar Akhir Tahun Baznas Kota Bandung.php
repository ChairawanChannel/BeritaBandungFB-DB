        <div class="detailcontent text-black">
            <p>Onang, pria paruh baya jadi salah satu pengunjung yang mencoba jasa tersebut. Setelah dicukur rapi dan dibersihkan dari sisa-sisa rambut, ia menceritakan tertarik ikut program yang diusung Baznas Kota Bandung di acara Gebyar Akhir Tahun Baznas Kota Bandung, Sabtu 23 Desember 2023.</p>

            <p>&quot;Program ini hasilnya akan disumbangkan ke Palestina. Saya tertarik ikut program cukur gratis bayar seikhlasnya, meski di dompet cuma punya Rp5.000,&quot; kata Onang.</p>

            <p>Baginya, program ini mengajarkan masyarakat untuk ikhlas memberikan harta yang dimiliki berapapun itu.&nbsp;</p>

            <p>&quot;Saya inginnya lebih daripada itu, tapi mampunya cuma bisa bayar segitu,&quot; ungkapnya.</p>

            <p>Meski dibayar seikhlasnya, tapi ia mengaku pelayanan para tukang cukur tetap maksimal.&nbsp;</p>

            <p>&quot;Tukang cukurnya juga ahli, ini dari Persatuan Pangkas Rambut Garut (PPRG). Kalau dekat, saya ingin datang. Tapi karena jauh, saya tinggal di Buahbatu, ini tempat cukurnya di Cileunyi,&quot; jelasnya.</p>

            <p>Sementara itu, Ketua Baznas Kota Bandung, Akhmad Roziqin menyampaikan, pada Gebyar Akhir tahun ini ada pula pembagian zakat infak sedekah (ZIS) kepada ratusan penerima manfaat (mustahik).</p>

            <p>&quot;ZIS yg ditipkan melalui Baznas, kami selenggarakan secara masif sebagai bentuk komitmen kami untuk menyampaikan titipan tersebut dengan penuh amanah,&quot; ucap Akhmad.</p>

            <p>Termasuk donasi kemanusiaan untuk Palestina yang telah terhimpun pada tahap pertama sebesar Rp1,7 miliar dan sudah diserahkan ke Baznas RI.&nbsp;</p>

            <p>Ia mengatakan, donasi tersebut masih tetap dihimpun sepanjang problem kemanusiaan di Palestina masih terjadi.</p>

            <p>&quot;Ini merupakan wujud keberpihakan kita terhadap kemanusiaan. Pada harta yang Allah berikan kepada kita sesungguhnya ada sebagian hak fakir miskin,&quot; tuturnya.</p>

            <p>Selain itu, Kepala Bagian Kesejahteraan Rakyat (Kesra), Momon A. Imron berharap, kegiatan ini bisa meningkatkan ZIS di Kota Bandung, sehingga semakin banyak masyarakat yang bisa merasakan manfaatnya.</p>

            <p>&quot;Dari tahun ke tahun dana zakat yang dikelola Baznas kota Bandung terus meningkat. Tahun lalu dana yang terhimpun sebesar Rp29 miliar. Insyaallah tahun ini akan semakin besar lagi,&quot; harap Momon.</p>

            <p>Ia menambahkan, bukan hanya mengumpulkan ZIS, tapi Baznas juga menyalurkannya secara tepat sasaran. Hal ini mampu menumbuhkan kepercayaan masyarakat untuk menitipkan dana ZIS kepada Baznas Kota Bandung.</p>

            <p>&quot;Baznas telah membantu Pemerintah Kota Bandung dalam upaya menyejahterakan masyarakat. Ke depan semoga Baznas bisa menyosialisasikan program kepada masyarakat lebih luas, sehingga banyak yang jadi muzaki dan semakin banyak mustahik yang terbantu,&quot; ungkapnya.</p>

            <p>Ia juga berpesan, agar dalam penyaluran ZIS harus bisa meningkatkan taraf hidup mustahik sehingga bisa naik menjadi muzaki.</p>

            <p>&quot;Ini berarti bantuan yang diberikan bukan hanya memenuhi kebutuhan hidup saja. Tapi berkelanjutan dan mampu dikembangkan oleh penerimanya,&quot; ujarnya.</p>

            <p>Dalam kesempatan tersebut, sebanyak 200 guru ngaji mendapatkan bantuan sosial, 100 anak mendapatkan bantuan pendidikan untuk tingkat SD.</p>

            <p>Lalu 100 bantuan pendidikan untuk siswa tingkat SMP, 100 bantuan pendidikan untuk siswa tingkat SMA, batuan lansia, dan bantuan yatim.</p>

            <p>Serta bantuan sembako untuk 200 warga duafa di Kota Bandung, rutilahu, dan kesehatan berupa kursi roda, kaki palsu<br />
                Bantuan modal serta sarana usaha.</p>

            <p>Salah satu penerima bantuan kesehatan adalah Asep Riana. Warga Babakan Ciparay ini mendapatkan bantuan kaki palsu sebelah kanan.</p>

            <p>&quot;Saya senang dapat bantuan kaki palsu ini. Akhirnya bisa beraktivitas dengan lebih nyaman. Sekarang saya bekerja di bidang konveksi, jadi penjahit,&quot; aku Asep.</p>

            <p>Asep telah menjalani hidupnya dengan kondisi kaki seperti ini sejak tahun 2011 karena mengalami kecelakaan lalu lintas.&nbsp;</p>

            <p>Ia berharap, ke depannya Pemerintah Kota Bandung dan Baznas bisa lebih banyak lagi membantu masyarakat yang membutuhkan. (din)**</p>

            <p><br />
                Kepala Diskominfo Kota Bandung</p>

